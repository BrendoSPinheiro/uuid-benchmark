'use strict'

const fs = require('fs')
const {Suite} = require('benchmark')
const numeral = require('numeral')

const suite = new Suite()

suite.cacheSizes = [16 ** 2, 16 ** 3, 16 ** 4]

require('./benchmarks/uuid')(suite)
require('./benchmarks/fast-uuid')(suite)
require('./benchmarks/uuid-random')(suite)
require('./benchmarks/sodium-uuid')(suite)
require('./benchmarks/crypto.randomBytes')(suite)
require('./benchmarks/crypto.randomFillSync')(suite)
require('./benchmarks/crypto.randomFill')(suite)
require('./benchmarks/dev-random')(suite)

const readmePath = 'README.md'
const csvPath = 'results.csv'
let csv = ''
let readme = fs.readFileSync(readmePath)
const startMarker = '<!-- AUTOGENERATED - DO NOT EDIT - START -->\n'
const endMarker = '<!-- AUTOGENERATED - DO NOT EDIT - END -->\n'
const startIndex = readme.indexOf(startMarker)
const endIndex = readme.indexOf(endMarker) + endMarker.length
const postChunk = readme.slice(endIndex)
readme = readme.slice(0, startIndex)

suite
  .on('start', function () {
    readme += startMarker
    readme += '| Method | Secure | Format | Re-use | Cache | Sync | Ops/sec | RME | Samples |\n'
    readme += '|--------|--------|--------|--------|-------|------|---------|-----|---------|\n'

    csv += '"Method","Secure","Format","Re-use","Cache","Sync","Ops/sec","Deviation","Mean","MOE","RME","Samples","SEM","Variance"\n'
  })
  .on('cycle', function (event) {
    const t = event.target
    console.log(`${genName(t)} x ${numeral(t.hz).format('0,0')} ops/sec ±${t.stats.rme.toFixed(2)}% (${t.stats.sample.length} runs sampled)`)
    readme += `| [${t.name}] | ${check(t.secure)} | ${t.format} | ${check(t.reuse)} | ${t.cacheSize || 'n/a'} | ${check(!t.defer)} | ${numeral(t.hz).format('0,0')} | ±${t.stats.rme.toFixed(2)}% | ${t.stats.sample.length} |\n`
    csv += `"${t.name}","${t.secure ? 'Y' : 'N'}","${t.format}","${t.reuse ? 'Y' : 'N'}",${t.cacheSize || ''},"${t.defer ? 'N' : 'Y'}",${t.hz},${t.stats.deviation},${t.stats.mean},${t.stats.moe},${t.stats.rme},${t.stats.sample.length},${t.stats.sem},${t.stats.variance}\n`
  })
  .on('complete', function () {
    console.log('Fastest is ' + genName(this.filter('fastest')[0]))

    readme += endMarker
    readme += postChunk
    fs.writeFileSync(readmePath, readme)
    fs.writeFileSync(csvPath, csv)
  })
  .run()

function genName (t) {
  return `${t.name} (format: ${t.format}, re-use: ${!!t.reuse}, cache: ${t.cacheSize || 'n/a'}, sync: ${!t.defer})`
}

function check (bool) {
  return bool ? '✅' : ''
}
